require "TimedActions/ISBaseTimedAction"
DoorBreacher = DoorBreacher or {}

ISDoorBreacher = ISBaseTimedAction:derive("ISDoorBreacher")

function ISDoorBreacher:isValid()
	if self.door:isBarricaded() then return false end
	local props = self.spr:getProperties()
	if props and props:Is("GarageDoor") then return false end
	local keyId = self.door:checkKeyId() or self.door:getKeyId()
	if self.character:getInventory():haveThisKeyId(keyId) then return false end
	return self.character:getPerkLevel(Perks.Strength) >= 8
end

function ISDoorBreacher:waitToStart()
    self.character:faceThisObject(self.door)
    return self.character:shouldBeTurning()
end

function ISDoorBreacher:update()

end

function ISDoorBreacher:start()
	self:setActionAnim("BreachDoor")
end

function ISDoorBreacher:stop()
    ISBaseTimedAction.stop(self)
end

function ISDoorBreacher:perform()
	local doorBreak = false
	--if SandboxVars.DoorBreacher.BreachCanBreak == true then end
	local roll = ZombRand(1,11)
	if roll > 5 then
		doorBreak = true
	end

	if doorBreak == true then
		if isClient() then
			sledgeDestroy(self.door)
		else
			self.door:getSquare():transmitRemoveItemFromSquare(self.door)
		end
		getSoundManager():PlayWorldSound("BreakObject", self.door:getSquare(), 0, 5, 5, false);
		addSound(self.door, self.door:getX(),self.door:getY(), self.door:getZ(), 15, 1);

	else
		door:setLockedByKey(false)
		door:ToggleDoor(self.character)
		getSoundManager():PlayWorldSound(self.door:getThumpSound(), self.door:getSquare(), 0, 5, 5, false);
		addSound(self.door, self.door:getX(),self.door:getY(), self.door:getZ(), 5, 1);
		--self.character:Say(tostring('Unlocked!'))
	end
	DoorBreacher.doBreachImpact(self.door)
	ISBaseTimedAction.perform(self);
end

function ISDoorBreacher:new(character, door, spr)
    local o = {}
    setmetatable(o, self)
    self.__index = self
    o.character = character
	o.door = door;
	o.stopOnRun = true;
	o.stopOnAim = true;
	o.stopOnWalk = true;
	o.action = "BreachDoor"
    o.skipBuildAction = false;
	o.spr = spr
    o.maxTime = 20
    return o
end
